/** @type {Array<{id: string, name: string, gId: string, gName: string, dId: string, dName: string}>} */
let ALL_TAGS = [];
/** @type {Object<string, {id: string, name: string, gId: string, gName: string, dId: string, dName: string}>} */
let TM = {};

function rebuildTags(dims) {
  ALL_TAGS = []; TM = {};
  dims.forEach(dim => {
    dim.groups.forEach(g => {
      g.tagObjs = g.tags.map(name => {
        const id = `t${++_tid}`;
        const o = { id, name, gId: g.id, gName: g.name, dId: dim.id, dName: dim.name };
        ALL_TAGS.push(o);
        TM[id] = o;
        return o;
      });
    });
  });
}
